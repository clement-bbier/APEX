//! apex_benches — scaffolding crate for criterion benchmark stubs.
//!
//! This crate has no library code yet. It exists purely so that the
//! workspace has a home for `benches/*.rs` criterion harnesses that
//! will drive Phase B Rust ports of Python hot-path calculators.
//!
//! Each bench file records the Python baseline (see
//! `benchmarks/results/baseline_2026-04-21.md`) and the target Rust
//! latency it must beat. The benches themselves are `todo!()` stubs
//! until the actual ports land.
